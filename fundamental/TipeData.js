// Undefined
let a;
console.log(typeof a);

// Numbers
let b = 100;
console.log(typeof b);

let c = 10.3;
console.log(typeof c);

// Aritmatika Numbers
let d = 10;
let e = 2;

console.log(d + e); // Penjumlahan
console.log(d - e); // Pengurangan
console.log(d * e); // Perkalian
console.log(d / e); // Pembagian
console.log(d % e); // Sisa Bagi
console.log(d ** e); // Perpangkatan

// Increment dan Decrement
// Post Increment
let postFix = 5;
console.log(postFix++);
console.log(postFix);

// Pre Decrement
let preFix = 5;
console.log(--preFix);

// BigInt
const bigNumber = 1234567890123456789012345678901234567890n;
const myInt = 1234567890123456789012345678901234567890;

console.log(bigNumber);
console.log(myInt);

// Strings
let color = "Blue";
console.log(typeof color);
console.log(`Sky color is ${color}`);

// Boolean
let benar = true;
let salah = false;

console.log(typeof benar);
console.log(typeof salah);
