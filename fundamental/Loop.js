// For Loop
for (i = 0; i <= 5; i++) {
  console.log(i);
}

// For Of
let myArray = ["Jhon", "Owi", "Soehar"];

for (const arrayItems of myArray) {
  console.log(arrayItems);
}

// While
let o = 1;
while (o <= 5) {
  console.log(o);
  o++;
}

// Do While
let p = 1;
do {
  console.log(p);
  p++;
} while (p <= 5);
